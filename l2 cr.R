# Construct the list
mixed_list <- list(
  numeric_element = 123,
  character_element = "Hello",
  logical_element = TRUE
)

# Print the list
print(mixed_list)


# Access and print a specific element
print("Character element:")
print(mixed_list$character_element)


# Calculate and display the length of the list
list_length <- length(mixed_list)
print("Length of the list:")
print(list_length)

